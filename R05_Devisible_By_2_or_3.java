public class R05_Devisible_By_2_or_3 {
    public static void main(String args[]) {
        int a=2;
        if (a%2==0)
            System.out.println("Devisible by 2.");
        else if (a%3==0)
            System.out.println("Devisible by 3.");
        else
            System.out.println("Devisible by neither 2 nor 3.");
    }
}
